package com.elenilson.cursojava.aula29;

public class Carro2 {
	String marca;
	String modelo;
	int numPassageiros; // Número de passageiros
	double capCombustivel; // Capacidade de tanque de combustível
	double consumoCombustivel; // Consumo de combustível por km
	
	public Carro2() {
		
	}
	
	public Carro2(String marca, String modelo, int numPassageiros, double capCombustivel, double consumoCombustivel) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.numPassageiros = numPassageiros;
		this.capCombustivel = capCombustivel;
		this.consumoCombustivel = consumoCombustivel;
	}
	

}
